# Initialize student index
students = ["bobby", "sally", "linda"]

print(students)
# output: ['bobby', 'sally', 'linda']

student1 = students[0]

print(student1)
# output: bobby

student1ThruStudent2 = students[0:2]

print(student1ThruStudent2)
# output: ['bobby', 'sally']

# Initialize string of all student names
studentString = '"bobby", "sally", "linda"'

print(studentString.upper())
# output: "BOBBY", "SALLY", "LINDA"

